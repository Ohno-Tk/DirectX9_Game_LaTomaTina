#define _CRT_SECURE_NO_WARNINGS

/*-----------------------------------------------------------------------------
	ヘッダファイル
-----------------------------------------------------------------------------*/
#include <windows.h>
#include <stdio.h>
#include "DebugConsole.h"

/*-----------------------------------------------------------------------------
 関数名:	void CDebugConsole::Init(void)
 引数:		
 戻り値:	
 説明:		初期化
-----------------------------------------------------------------------------*/
void CDebugConsole::Init(void)
{

	AllocConsole();	// コンソールを作成

	freopen("CON", "r", stdin);     // 標準入力の割り当て
	freopen("CON", "w", stdout);    // 標準出力の割り当て

	printf("Debug");
}

/*-----------------------------------------------------------------------------
関数名:		void CDebugConsole::Uninit(void)
引数:
戻り値:
説明:		終了
-----------------------------------------------------------------------------*/
void CDebugConsole::Uninit(void)
{
	FreeConsole();
}