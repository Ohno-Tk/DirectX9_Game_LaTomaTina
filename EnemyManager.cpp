void CEnemyManager::Update(void)
{

	m_Cont++;

	int i = rand() % 50 + 100;

	if (m_Cont > i)
	{
		float speed = (float)(rand() % 2);

		float pos = (float)(rand() % 7 - 7);

		CEnemy::Create(D3DXVECTOR3(pos,-5.0f,0.0f), 0.5f+ speed);

		m_Cont = 0;
	}
}